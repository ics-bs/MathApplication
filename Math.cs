using System;
using System.Collections.Generic;
using System.Linq;
using System.Text;
using System.Threading.Tasks;

namespace MathApplication
{
    class Math
    {
        public int Subtract(int x, int y)
        {
            return y - x;
        }

        public int Multiplication(int x, int y)
        {
            // This is a multiplication of x and y
            return x * y;
        }
    }
}
